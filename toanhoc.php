*Phân tích, thiết kế cơ sở dữ liệu, lập trình viên, nghiên cứu viên tại các viện nghiên cứu trên cả nước.<br>
*Giảng dạy Toán học, tại các trường đại học, cao đẳng, trung học chuyên nghiệp, dạy nghề, các trung tâm tin học và các trường trung học phổ thông.<br>
*Có khả năng tiếp tục học thêm hoặc tìm kiếm học bổng về các chương trình đào tạo nghiên cứu  chuyên sâu như Thạc sĩ, Tiến sĩ các chuyên ngành của ngành Toán học, Quản trị, Kinh tế và Khoa học máy tính tại các cơ sở đào tạo trong và ngoài nước.<br>

